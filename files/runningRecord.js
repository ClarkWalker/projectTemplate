function bmi(weight, height) {
  let squareHeight = height^2;
  console.log(`squareHeight =`, squareHeight);
  let bmindex = weight / squareHeight;
  console.log(bmindex);
  if (bmindex <= 18.5) {
    return `Underweight`;
  }
  if (bmindex <= 25.0) {
    return `Normal`;
  }
  if (bmindex <= 30.0) {
    return `Overweight`;
  }
  if (bmindex > 30) {
    return `Obese`;
  }
}

